/*Write a program to make addition, Subtraction, Multiplication and Division of Two Numbers.*/
void main()
{
  int num1 = 20;
  int num2 = 10;

  int add = num1 + num2;
  int sub = num1 - num2;
  int mul = num1 * num2;
  double div = num1 / num2;
  int mod = num1 % num2;

  print(add);
  print(sub);
  print(mul);
  print(div);
  print(mod);
}
